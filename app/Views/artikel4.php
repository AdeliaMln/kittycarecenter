                            <p>Apakah kucingmu sangat sering menggaruk tubuhnya?
                                Jika kucingmu sangat sering menggaruk-garuk tubuhnya, maka bisa jadi kucingmu terkena penyakit kulit.
                                Penyakit kulit yang mungkin saja terjadi pada kucing bisa berbagai macam,
                                berikut ini adalah beberapa penyakit kulit yang sering terjadi pada kucing dan cara mengobati dan mencegahnya.
                                <br>
                                <br>
                                <b>1. PENYAKIT KULIT KUCING RING WORM</b>
                                <br>
                                <br>
                                Ringworm adalah jenis lain dari jamur yang menyerang kucing, terutama anak kucing di bawah satu tahun.
                                Hal ini menyebabkan lesi melingkar di kepala kucing, telinga, dan badan.
                                Kulit di sekitar lesi sering bersisik dan botak.
                                Ringworm sangat menular dan dapat menyebar ke hewan peliharaan lain di rumah, serta kepada orang-orang sekitar.
                                <br>
                                <br>
                                Pengobatan tergantung pada tingkat keparahan, tetapi mungkin termasuk shampoo khusus, salep, atau obat-obatan oral.
                                <ol>
                                    <li>Sebulan sekali Lakukan desinfektan Rutin ruangan dan kandang tempat tinggal kucing dengan cairan Bleaching /pemutih ,
                                        dan desinfektan untuk hewan.</li>
                                    <li>Sebulan sekali Mandikan atau rendam kucing dalam cairan Lyme sulfur/larutan belerang. atau air 10 lt yang di tetesi propolis 20 tetes
                                        atau mandikan dengan menggunakan shampo Sebazole.</li>
                                </ol>
                                Pengobatan Ring Worm Kucing:
                                <ol>
                                    <li>Bersihkan kerak yang terbentuk dengan Minyak Tawon atau Virgin Coconut Oil (VCO),
                                        setelah bersih oleskan Propolis atau salep anti Jamur biasanya Ketokenazole.</li>
                                    <li>Untuk kucing diatas 4 Bulan secara Oral berikan Ithraconazole/ Sporanox,
                                        satu kapsul di bagi 5 selama 2 minggu sehari sekali.</li>
                                    <li>Untuk kucing di bawah 4 bulan atau Hamil, jangan di berikan obat oral.</li>
                                </ol>
                                <br>
                                <b>2. KULIT KERING, KETOMBE PADA KUCING</b>
                                <br>
                                <br>
                                Seperti manusia, kulit kucing juga dapat kering, dan terkelupas.
                                Ini biasanya tidak ada yang serius, tetapi tidak ada salahnya teman-teman konsultasikan dengan dokter hewan.
                                Ketombe yang terus menerus dapat menjadi tanda gizi buruk, perawatan yang tidak memadai, atau ada masalah Kesehatan.
                                Shampo khusus dan suplemen omega-3 asam lemak dapat membantu mengobati ketombe kucing.
                                <br>
                                <br>
                                Pencegahan dan Pengobatan Penyakit Kulit Kering Kucing:
                                <br>
                                <ol>
                                    <li>Mandikan Kucing Sebulan sekali dengan shampo anti jamur /sebazole.</li>
                                    <li>Berikan Suplemen tambahan seperti Minyak ikan dan X trabloom wate.</li>
                                </ol>
                                <br>
                                <b>3. INFEKSI JAMUR (YEAST) PADA KUCING PERSIA</b>
                                <br>
                                <br>
                                Infeksi jamur yang disebabkan oleh jamur Banyak terjadi pada kucing yang memiliki masalah medis lainnya.
                                Telinga adalah salah satu tempat yang paling umum untuk infeksi jamur.
                                Gejala mungkin termasuk cairan hitam atau kuning, kemerahan pada daun telinga, dan terus-menerus mengaruk telinga.
                                Infeksi jamur mudah didiagnosis dan biasanya cepat sembuh terhadap pengobatan dengan obat anti jamur.
                                <br>
                                <br>
                                Pencegahan Penyakit Kulit Jamur Kucing:
                                <ol>
                                    <li>Untuk melakukan pencegahan pada penyakit kulit kucing yang satu ini, usahakan dalam sebulan sekali
                                        lakukan disinfektan rutin ruangan dan kandang tempat tinggal kucing dengan cairan Bleaching /pemutih,
                                        dan disinfektan untuk hewan.</li>
                                    <li>Sebulan sekali mandikan atau rendam kucing dalam cairan Lyme sulfur/larutan belerang,
                                        atau air 10 lt yang di menetesi pro polis 20 tetes
                                        atau mandikan dengan menggunakan shampo Sebazole.</li>
                                </ol>
                                Pengobatan Penyakit Kucing Jamur :
                                <ol>
                                    <li>Bersihkan kerak yang terbentuk dengan Minyak Tawon atau Virgin Coconut Oil (VCO),
                                        setelah bersih oleskan Propolis atau salep anti Jamur biasanya Ketokenazole.</li>
                                    <li>Untuk kucing diatas 4 Bulan secara Oral berikan Ithraconazole/ Sporanox,
                                        satu kapsul di bagi 5 selama 2 minggu sehari sekali.</li>
                                    <li>Untuk kucing di bawah 4 bulan atau Hamil, jangan di berikan obat oral.</li>
                                </ol>
                                <br>
                                <b>4. PENYAKIT KULIT JERAWAT KUCING</b>
                                <br>
                                <br>
                                Jerawat biasanya muncul di dan di sekitar dagu kucing.
                                Kemungkinan penyebab termasuk stres, jarang dibersihkan, reaksi terhadap obat, atau masalah kulit.
                                Dokter hewan teman-teman dapat merekomendasikan shampoo khusus atau gel untuk membersihkan Jerawat,
                                atau antibiotik jika infeksi bakteri menyertai jerawat.
                                <br>
                                <br>
                                Pengobatan Penyakit Kucing Jerawat :
                                <ol>
                                    <li>Bersihkan kerak yang terbentuk dengan Minyak Tawon atau Virgin Coconut Oil (VCO),
                                        setelah bersih oleskan Pro polis.</li>
                                </ol>
                                <br>
                                <b>5. PENYAKIT KUCING EOSINOPHILIC GRANULOMA</b>
                                <br>
                                <br>
                                Ini jika kucing persia teman-teman memiliki borok atau luka pada hidung atau bibir,
                                ia mungkin akan mengalami jenis reaksi alergi yang dikenal sebagai granuloma eosinofilik.
                                Reaksi ini dapat terjadi di mana saja pada tubuh, tetapi paling sering pada wajah, bantalan kaki, dan paha.
                                Alergi makanan atau kutu kadang-kadang merupakan penyebabnya tetapi luka ini juga bisa terjadi akibat infeksi bakteri.
                                Pengobatannya tergantung pada apa yang menyebabkannya.
                                <br>
                                <br>
                                Pengobatan Penyakit Borok Kucing Persia:
                                <ol>
                                    <li>Lakukan bersihkan kerak yang terbentuk dengan Minyak Tawon atau Virgin Coconut Oil (VCO),
                                        setelah bersih oleskan Propolis.</li>
                                </ol>
                                <br>
                                <b>6. PENYAKIT KULIT KUCING ALERGIC DERMATITIS</b>
                                <br>
                                <br>
                                Kucing dapat memiliki reaksi alergi terhadap produk perawatan, makanan, dan gangguan lingkungan,
                                seperti serbuk sari atau gigitan kutu.
                                Dengan melakukan garukkan pada bagian kepala atau leher adalah tanda umum dari alergi makanan.
                                Gejala alergi lainnya termasuk menjilat pada kaki atau menggaruk telinga atau pangkal ekor.
                                Ada berbagai cara untuk mengobati kulit gatal berhubungan dengan alergi, tapi melakukan pencegahan adalah
                                strategi terbaik.
                                <br>
                                <br>
                                Pengobatan penyakit alergi pada kucing persia :
                                <ol>
                                    <li>Bersihkan kerak yang terbentuk dengan Minyak Tawon atau Virgin Coconut Oil (VCO),
                                        setelah bersih oleskan Propolis.</li>
                                </ol>
                                <br>
                                <b>7. PENYAKIT KULIT KUCING STUD TAIL</b>
                                <br>
                                <br>
                                Ini juga disebut hiperplasia kelenjar pada buntut, ekor kucing persia pejantan akan mengeluarkan kelenjar minyak
                                yang aktif di bagian atas ekor. Kelenjar ini menghasilkan ekskresi lilin yang Membuat rambut rontok dan lesi berkerak.
                                Dalam kasus yang parah, kondisi bisa membuat ekor rentan terhadap infeksi bakteri.
                                Neutering/Kebiri dapat menghilangkan masalah pada kucing jantan. Pilihan pengobatan lain adalah rajin merawat ekor
                                dan menggunakan sampo yang diformulasikan secara khusus.
                                <br>
                                <br>
                                Pengobatan Penyakit Kulit Kucing stud tail:
                                <ol>
                                    <li>Cuci/bersihkan lemak di buntut dengan menggunakan Goop atau sabun untuk cuci piring.</li>
                                    <li>Setelah bersih bilas dengan shampo sebazole dan terakhir bilas dengan air propolis
                                        (20 tetes propolis dan 1 liter air), keringkan sampai kering.</li>
                                </ol>
                                <br>
                                <b>8. PENYAKIT KULIT RAMBUT RONTOK PADA KUCING</b>
                                <br>
                                <br>
                                Ini terjadi jika catlovers sekalian melihat kucing teman-teman kehilangan rambut lebih dari biasanya atau
                                mendapati bagian yang botak, kemungkinan terjadi penyakit kulit pada kucingmu.
                                Segera mungkin temui dokter hewan teman-teman.
                                Rambut rontok abnormal dapat menjadi tanda peringatan dari beberapa penyakit, adanya kutu, stres, alergi,
                                atau gizi buruk.
                                <br>
                                <br>
                                Pengobatan Rambut Rontok Pada Kucing:
                                <ol>
                                    <li>Segera berikan makanan bergizi misalnya wet food dicampur kitty bloom atau
                                        kalau untuk menggemukan berikan juga Xtra Bloom wate.</li>
                                    <li>Juga lakukanlah pengobatan sesuai dengan penyebab kebotakan.</li>
                                </ol>
                                <br>
                                <b>9. PENYAKIT KULIT INFEKSI BAKTERI KUCING</b>
                                <br>
                                <br>
                                Adalah dalam banyak kasus, infeksi kulit bakteri berkembang sebagai akibat dari masalah kulit lainnya.
                                Contohnya adalah , jerawat kucing dapat mengakibatkan terjadinya akar rambut kucing persia atau
                                anggora teman-teman lebih rentan terhadap infeksi, sehingga folliculitis.
                                Infeksi bakteri dapat diobati dengan antibiotik, tetapi yang terpenting menjaga kesehatan kulit agar
                                tidak terkena infeksi bakteri.
                                <br>
                                <br>
                                Pengobatan penyakit kulit infeksi kucing:
                                <ol>
                                    <li>Bersihkan kerak yang terbentuk dengan Minyak Tawon atau Virgin Coconut Oil (VCO),
                                        setelah bersih oleskan Propolis atau salep anti bakteri misalnya kemicetine salep.</li>
                                    <li>Berikan antibiotik sesuai anjuran dr hewan teman-teman.</li>
                                </ol>
                                <br>
                                <b>10. PENYAKIT KULIT SCABIES KUCING</b>
                                <br>
                                <br>
                                Yang dimaksud dengan Scabies kucing adalah yaitu penyakit kulit biasa yang disebabkan oleh
                                tungau pada kepala Notoedres cati. Adapun yang menjadi gejala pertama adalah rasa gatal sekitar kepala dan leher,
                                bersama dengan rambut rontok dan munculnya bintik botak.
                                Karena sering nya menggaruk, kulit menjadi merah, dan luka.
                                Biasanya, teman-teman akan melihat warna abu-abu tebal untuk kerak kuning di sekitar, leher tepi wajah, dan telinga.
                                Kondisi ini juga dapat terjadi pada kulit kaki dan alat kelamin.
                                <br>
                                <br>
                                Pengobatan Penyakit Scabies Kucing Persia atau Anggora:
                                <ol>
                                    <li>Lakukanlah membersihkan kerak yang terbentuk dengan media Minyak Tawon atau Virgin Coconut Oil (VCO),
                                        setelah bersih oleskan Propolis.</li>
                                    <li>Untuk Ear mite dalam kuping bersihkan dengan memasukkan cairan pembersih telinga Otolin.
                                        Bisa juga di menetesi obat kutu frontline di tengkuk kucing.</li>
                                    <li>Untuk Kucing persia atau anggora dewasa bila scabies sudah menyebar konsultasi kan dengan dokter hewan teman-teman,
                                        karena di perlukan penyuntikan yaitu : anti parasit.</li>
                                    <li>Sedangkan untuk kitten atau bayi pengobatan dengan membersihkan kerak yang terbentuk dengan Minyak Tawon atau
                                        Virgin Coconut Oil (VCO), setelah bersih oleskan Propolis, merupakan pilihan terbaik.</li>
                                </ol>
                                <br>
                                <b>11. PENYAKIT KULIT KUTU LICE</b>
                                <br>
                                <br>
                                Penyakit kulit ini adalah Kutu Lice adalah parasit yang membuat kulit kering.
                                Mereka biasanya ditemukan pada kucing persia tua atau sakit dan sering pergi tanpa diketahui.
                                Hal ini dapat membuat kucing anggora melakukan penggarukkan, gelisah, penampilan rambut yang tidak biasa,
                                dan rambut rontok. Seperti tungau, kutu Lice dapat diobati dengan pengobatan topikal.
                                Karena kutu spesies-spesifik, teman-teman tidak perlu khawatir akan tertular kutu dari kucingmu.
                                <br>
                                <br>
                                Pengobatan Penyakit Kulit Kutu Lice :
                                <ol>
                                    <li>Langkah pertama yang dapat teman-teman lakukan yaitu dengan cara membersihkan kerak2 yang terbentuk dengan
                                        Minyak Tawon atau Virgin Coconut Oil (VCO), dan setelah bersih ,lalu segera teman-teman dapat oleskan Propolis,
                                        teteskan tengkuk kucing dengan obat kutu seperti frontline.</li>
                                    <li>Dan untuk Kucing dewasa bila scabies sudah menyebar konsultasikan dengan dokter hewan teman-teman,
                                        karena di perlukan penyuntikan anti parasit.</li>
                                    <li>Sedangkan untuk kitten atau bayi pengobatan dengan membersihkan kerak2 yang terbentuk dengan
                                        Minyak Tawon atau Virgin Coconut Oil (VCO), setelah bersih oleskan Propolis, merupakan pilihan terbaik.</li>
                                </ol>
                                <br>
                                <b>12. PENYAKIT KULIT KUTU KUCING</b>
                                <br>
                                <br>
                                Kutu/ Pinjal adalah masalah yang sangat umum ditemui pada kucing persia atau anggora kesayangan teman-teman sekalian,
                                jadi jangan khawatir mengenai hal ini teman-teman dapat mencari mereka atau telur mereka dalam rambut kucing persia
                                peliharaanmu, terutama di mana warna bulu pucat. Tanda-tanda lain adanya adalah bila kucing teman-teman sering
                                menggaruk, luka kulit berkerak, dan penipisan rambut di atas pangkal ekor.
                                Untuk membasmi kutu, teman-teman harus merawat kucingmu, serta furniturnya, tempat tidur, dan karpet.
                                Melakukan desinfektan bulanan adalah yang terbaik.
                                <br>
                                <br>
                                Pengobatan Penyakit kulit Kutu Kucing:
                                <ol>
                                    <li>Lakukanlah membersihkan kerak2 atau kotoran dengan VCO atau minyak tawon, oleskan propolis.</li>
                                    <li>Teteskan obat Kutu Frontline Pada daerah tengkuk Kucing persia teman-teman.</li>
                                    <li>Desinfektan ruangan atau kandang kucing minimal sebulan sekali.</li>
                                </ol>
